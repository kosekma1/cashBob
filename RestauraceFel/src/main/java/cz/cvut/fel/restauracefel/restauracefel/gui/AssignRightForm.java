    /**
     * Metoda kontrolujici spravnost vyplnenych udaju.
     *
     * @return Vraci index urcujici vstupni komponentu, ktera obsahuje
     * neplatny vstup. Pokud je vse vporadku tak navraci 0.
     */
    @Override
    protected int isValidInput() {
        if (!Validator.isText(jTextFieldRole)) {
            return 1;
        }
        if (!Validator.isPositiveInt(jTextFieldAddedRights) && !Validator.isPositiveInt(jTextFieldRemovedRights)) {
            return 2;
        }
        return 0;
    }

    /**
     * Metoda ulozi seznam vybranych prav k pridani predavany z dialogu
     * ChooseRightDialog v parametru addedRights.
     *
     * @param addedRights
     */
    protected void fillAddedRights(HashSet<String> addedRights) {
        this.addedRights = addedRights;
        jTextFieldAddedRights.setText(Integer.toString(addedRights.size()));
    }

    /**
     * Metoda ulozi seznam vybranych prav k odebrani predavany z dialogu
     * ChooseRightDialog v parametru removedRights.
     *
     * @param addedRights
     */
