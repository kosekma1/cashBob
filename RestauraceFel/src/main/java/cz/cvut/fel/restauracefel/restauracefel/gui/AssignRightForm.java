    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        int i = isValidInput();
        switch (i) {
            case 0:
                try {
                    int roleId = CommonServiceFacade.getInstance().getRoleByName(jTextFieldRole.getText()).getRoleId();
                    Iterator it;
                    if (addedRights != null) {
                        it = addedRights.iterator();
                        while (it.hasNext()) {
                            String name = (String) it.next();
                            int rightId = CommonServiceFacade.getInstance().getRightByName(name).getRightId();
                            CommonServiceFacade.getInstance().createRoleRight(roleId, rightId);
                        }
                    }

                    if (removedRights != null) {
                        it = removedRights.iterator();
                        while (it.hasNext()) {
                            String name = (String) it.next();
                            int rightId = CommonServiceFacade.getInstance().getRightByName(name).getRightId();
                            CommonServiceFacade.getInstance().deleteRoleRight(roleId, rightId);
                        }
                    }
                } catch (FileNotFoundException fnfe) {
                    JOptionPane.showMessageDialog(this, "Konfigurační soubor \"" + ConfigParser.getConfigFile() + "\" nebyl nalezen.", "Chyba", JOptionPane.ERROR_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Nelze navázat spojení se serverem.", "Chyba komunikace", JOptionPane.ERROR_MESSAGE);
                }
                clearFields();
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Musí být vybrána uživatelská role.", "Úprava přístupových práv", JOptionPane.INFORMATION_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Musí být přidáváno nebo odebíráno aspoň 1 přístupové právo.", "Úprava přístupových práv", JOptionPane.INFORMATION_MESSAGE);
                break;
            default:
                break;
        }
}//GEN-LAST:event_jButtonOKActionPerformed

    private void jButtonRemoveRightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveRightsActionPerformed
        if (isValidInput() == 1) {
            JOptionPane.showMessageDialog(this, "K odebrání přístupových práv musí být vybrána uživatelská role.", "Odebrání přístupových práv", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try {
            int roleId = CommonServiceFacade.getInstance().getRoleByName(jTextFieldRole.getText()).getRoleId();
            chooseRightDialog = new ChooseRightDialog(parent, true, this, roleId);
            chooseRightDialog.setLocation(550, 210);
            chooseRightDialog.setVisible(true);
            refresh();
        } catch (EmptyListException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), ex.getTitle(), JOptionPane.INFORMATION_MESSAGE);
        } catch (FileNotFoundException fnfe) {
            JOptionPane.showMessageDialog(this, "Konfigurační soubor \"" + ConfigParser.getConfigFile() + "\" nebyl nalezen.", "Chyba", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Nelze navázat spojení se serverem.", "Chyba komunikace", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_jButtonRemoveRightsActionPerformed

