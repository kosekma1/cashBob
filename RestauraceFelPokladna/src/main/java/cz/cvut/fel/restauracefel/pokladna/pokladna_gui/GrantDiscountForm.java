    /**
     * Metoda kontrolujici spravnost vyplnenych udaju.
     *
     * @return Vraci index urcujici vstupni komponentu, ktera obsahuje
     * neplatny vstup. Pokud je vse vporadku tak navraci 0.
     */
    protected EnumValidace isValidInputComplex() {
        if (!Validator.isText(jTextFieldDiscountType)) return EnumValidace.Sleva;
        if (!Validator.isText(jTextFieldUser) && !Validator.isText(jTextFieldRole)) {
            return EnumValidace.Osoba;
        }
        return EnumValidace.Spravne;
    }

