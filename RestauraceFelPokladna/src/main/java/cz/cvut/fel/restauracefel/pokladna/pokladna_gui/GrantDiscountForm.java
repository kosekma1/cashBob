    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        EnumValidace i = isValidInputComplex();
        switch (i) {
            case Spravne:
                try {
                    int discountTypeId = ServiceFacade.getInstance().getDiscountTypeByName(jTextFieldDiscountType.getText()).getDiscountTypeId();
                    if (!jTextFieldUser.getText().equals("")) {
                        int userId = ServiceFacade.getInstance().getUserByUsername(jTextFieldUser.getText()).getUserId();
                        ServiceFacade.getInstance().createUserDiscountType(userId, discountTypeId);
                    } else if (!jTextFieldRole.getText().equals("")) {
                        int roleId = ServiceFacade.getInstance().getRoleByName(jTextFieldRole.getText()).getRoleId();
                        ServiceFacade.getInstance().createRoleDiscountType(roleId, discountTypeId);
                    }
                } catch (FileNotFoundException fnfe) {
                    JOptionPane.showMessageDialog(this, "Konfigurační soubor \"" + ConfigParser.getConfigFile() + "\" nebyl nalezen.", "Chyba", JOptionPane.ERROR_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Nelze navázat spojení se serverem.", "Chyba komunikace", JOptionPane.ERROR_MESSAGE);
                }
                clearFields();
                break;
            case Sleva:
                JOptionPane.showMessageDialog(this, "Musí být vybrán typ slevy.", "Přiřazení slevy", JOptionPane.INFORMATION_MESSAGE);
                break;
            case Osoba:
                JOptionPane.showMessageDialog(this, "Musí být vybrána osoba nebo uživatelská role, které se má daný typ slevy přiřadit.", "Přiřazení slevy", JOptionPane.INFORMATION_MESSAGE);
                break;
            default:
                break;
        }
}//GEN-LAST:event_jButtonOKActionPerformed

